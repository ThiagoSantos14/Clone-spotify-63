    const containerPhonk02 = document.querySelector('.contant-phonk-02');
    const containerPhonk03 = document.querySelector('.contant-phonk-03');

    const containerInternacional01 = document.querySelector('.contant-internacional-01');
    const containerInternacional02 = document.querySelector('.contant-internacional-02');
    const containerInternacional03 = document.querySelector('.contant-internacional-03');

    containerSertanejo01.style.display = "flex";
    containerSertanejo02.style.display = "flex";
    containerSertanejo03.style.display = "flex";

